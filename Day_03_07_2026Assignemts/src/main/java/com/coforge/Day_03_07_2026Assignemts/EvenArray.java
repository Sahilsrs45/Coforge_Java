package com.coforge.Day_03_07_2026Assignemts;

public class EvenArray {
	
	
	 public static void main(String[] args) {

	        int arr[] = {10, 15, 20, 25, 30, 35};

	        System.out.print("Even Numbers of array :");

	        for (int num : arr) {
	            if (num % 2 == 0) {
	                System.out.print(num + " ");
	            }
	        }
	    }

}
